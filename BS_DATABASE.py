STARTING_AD = datetime(1918, 4, 13)  # Corresponds to 1975-01-01 BS
STARTING_BS_YEAR = 1975
def gregorian_to_bs(gregorian_date):
    try:
        greg_dt = datetime.strptime(gregorian_date, "%Y-%m-%d")
        days_diff = (greg_dt - STARTING_AD).days
        if days_diff < 0:
            return "Out of Range"

        bs_year = STARTING_BS_YEAR
        total_days = 0

        for year in range(STARTING_BS_YEAR, 2092):
            if year not in BS_DATABASE:
                return "Out of Range"
            year_days = sum(BS_DATABASE[year])
            if total_days + year_days > days_diff:
                remaining_days = days_diff - total_days
                for month, month_days in enumerate(BS_DATABASE[year], 1):
                    if remaining_days < month_days:
                        bs_day = remaining_days + 1
                        return f"{bs_year:04d}-{month:02d}-{bs_day:02d}"
                    remaining_days -= month_days
            total_days += year_days
            bs_year += 1

        return "Out of Range"
    except:
        return "Invalid Date"

def bs_to_gregorian(bs_date):
    try:
        bs_year, bs_month, bs_day = map(int, bs_date.split('-'))
        if bs_year < STARTING_BS_YEAR or bs_year > 2091 or bs_month < 1 or bs_month > 12 or bs_day < 1 or bs_day > BS_DATABASE[bs_year][bs_month - 1]:
            return "Out of Range"

        days_diff = 0
        for year in range(STARTING_BS_YEAR, bs_year):
            days_diff += sum(BS_DATABASE[year])
        for month in range(bs_month - 1):
            days_diff += BS_DATABASE[bs_year][month]
        days_diff += bs_day - 1

        greg_dt = STARTING_AD + timedelta(days=days_diff)
        return greg_dt.strftime("%Y-%m-%d")
    except:
        return "Invalid Date"